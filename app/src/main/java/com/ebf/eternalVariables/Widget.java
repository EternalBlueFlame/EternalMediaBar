package com.ebf.eternalVariables;

public class Widget {
    public int ID =0;
    public int X =0;
    public int Y =0;
    public int width =0;
    public int height =0;

    public Widget(int ID, int width, int height){
        this.ID = ID;
        this.width = width;
        this.height = height;
    }
}
